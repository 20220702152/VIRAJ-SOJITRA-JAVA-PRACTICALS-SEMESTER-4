package Practical;
public class pract30_wrapperclass 
{

    public static void main(String[] args) 
    {
    	
        // Autoboxing
        int i = 10;
        Integer iObj = i; 
        // Unboxing
        int j = iObj; 

        System.out.println("i = " + i);
        System.out.println("iObj = " + iObj);
        System.out.println("j = " + j);
    }
}